// i++ , --i
#include <stdio.h>

int main() {
    int i=0,j=5;
    
    printf("%d\n",i);
    printf("%d\n",++i);
    printf("%d\n",i++);
    printf("%d\n",i);
    
    printf("\n");
    printf("\n");
    
    printf("%d\n",j);
    printf("%d\n",--j);
    printf("%d\n",j--);
    printf("%d\n",j);


    return 0;
}
